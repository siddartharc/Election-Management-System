public class Cast {
private String id;
private String aadhar;
public Cast() {}
public Cast(String id,String aadhar) {
	super();
	this.id = id;
	this.aadhar = aadhar;
}
public String getId() {
	return id;
}
public void setId(String id) {
	this.id = id;
}
public String getAadhar() {
	return aadhar;
}
public void setName(String aadhar) {
	this.aadhar = aadhar;
}
}